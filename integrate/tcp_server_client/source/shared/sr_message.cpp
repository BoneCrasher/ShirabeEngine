#include "sr_pch.h"

#include "tcp_server_client/shared/sr_message.h"

//<-----------------------------------------------------------------------------
//
//<-----------------------------------------------------------------------------
CTCPMessage CTCPMessage::create(std::string const &aMessage)
{
    return CTCPMessage(aMessage);
}
//<-----------------------------------------------------------------------------

//<-----------------------------------------------------------------------------
//<
//<-----------------------------------------------------------------------------
CTCPMessage::CTCPMessage()
  : mData(std::string())
{ }
//<-----------------------------------------------------------------------------

//<-----------------------------------------------------------------------------
//<
//<-----------------------------------------------------------------------------
CTCPMessage::SHeader const &CTCPMessage::getHeader() const
{
    return mHeader;
}
//<-----------------------------------------------------------------------------

//<-----------------------------------------------------------------------------
//<
//<-----------------------------------------------------------------------------
CTCPMessage::SHeader &CTCPMessage::getHeader()
{
    return mHeader;
}
//<-----------------------------------------------------------------------------

//<-----------------------------------------------------------------------------
//<
//<-----------------------------------------------------------------------------
std::string const &CTCPMessage::getData() const
{
    return mData;
}
//<-----------------------------------------------------------------------------

//<-----------------------------------------------------------------------------
//<
//<-----------------------------------------------------------------------------
std::string &CTCPMessage::getData()
{
    return mData;
}
//<-----------------------------------------------------------------------------

//<-----------------------------------------------------------------------------
//<
//<-----------------------------------------------------------------------------
std::size_t CTCPMessage::getLength() const
{
return (sizeof(SHeader) + getMessageSize());
}
//<-----------------------------------------------------------------------------

//<-----------------------------------------------------------------------------
//<
//<-----------------------------------------------------------------------------
std::size_t CTCPMessage::getMessageSize() const
{
    return mData.size();
}
//<-----------------------------------------------------------------------------

//<-----------------------------------------------------------------------------
//<
//<-----------------------------------------------------------------------------
CTCPMessage::CTCPMessage(std::string const &aMessage)
    : mHeader()
    , mData(aMessage)
{
    mHeader.mMessageLength = aMessage.size();
}
//<-----------------------------------------------------------------------------
